package Modelo;

public class EntMemorandos {
    
    String Codigo_Empleado;
    String Tipo_Memorando;
    String Descripcion_Memorando;

    public String getCodigo_Empleado() {
        return Codigo_Empleado;
    }

    public void setCodigo_Empleado(String Nombre_Profesor) {
        this.Codigo_Empleado = Nombre_Profesor;
    }

    public String getTipo_Memorando() {
        return Tipo_Memorando;
    }

    public void setTipo_Memorando(String Tipo_Memorando) {
        this.Tipo_Memorando = Tipo_Memorando;
    }

    public String getDescripcion_Memorando() {
        return Descripcion_Memorando;
    }

    public void setDescripcion_Memorando(String Descripcion_Memorando) {
        this.Descripcion_Memorando = Descripcion_Memorando;
    }
    
    
}
